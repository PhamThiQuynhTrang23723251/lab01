import random
print("số nguyên ngẫu nhiên từ 0 den 100 là: ",random.randint(0,100))
print("số nguyên ngẫu nhiên từ 50 den 99 là: ",random.randint(50,99))
print("số nguyên ngẫu nhiên từ -39 den 79 là: ",random.randint(-39,79))
print("số nguyên ngẫu nhiên từ -79 den -39 là: ",random.randint(-79,-39))
print("số thực ngẫu nhiên từ 0 den 100 là: ",random.uniform(0,100))
print("số thực ngẫu nhiên từ 50 den 99 là: ",random.uniform(50,99))
print("số thực ngẫu nhiên từ -39 den 79 là: ",random.uniform(-39,79))
print("số thực ngẫu nhiên từ -79 den -39 là: ",random.uniform(-79,-39))
